// Get All Menu

export const getAllMenu = async (mAm) => {
    const [result] = await pool.query(
        `
      SELECT * FROM menu 
      `);
      const rows = result[mAm];
      return rows; 
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Create Menu 

export const saveMenu = async (sMen) => {
	const result = await pool.query(
		`
        INSERT INTO menu(item_name, quantity, description, img)
         VALUES(?, ?, ?, ?, ?) 
    `,
		[sMen.id, sMen.item_name, sMen.quantity, sMen.description, sMen.img]
	);
	return result;
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Update Menu

export const updateMenu = async (uMen) => {
	const result = await pool.query(
		`
        UPDATE menu SET item_name = ?, quantity = ?, description  = ?, img = ?
        WHERE id = ?
    `,
		[uMen.item_name, uMen.quantity, uMen.description, uMen.img, uMen.id]
	);
	return result;
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Delete Menu

export const deleteMenu = async (dMen) => {
	const [row] = await pool.query(
		`
        DELETE FROM menu
        WHERE id=?
    `,
		[dMen]
	);
	return row;
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Get Single Menu 

export const getSingledMenu = async (aID) => {
	const result = await pool.query(
		`
        SELECT * FROM menu WHERE id = ?
    `,
		[aID]
	);
	const rows = result[0];
	return rows;
};
